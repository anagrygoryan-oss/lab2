#include <stdio.h> // Підключаємо бібліотеку для роботи з вводом/виводом (printf)

int main() {
    // 1. Оголошення змінних та присвоєння їм значень за варіантом
    // Використовуємо float (числа з комою), щоб ділення працювало правильно
    float a = 6;
    float b = 0;
    float c = 11;
    float y; // Змінна для збереження результату

    // 2. Виведення інформації про роботу в консоль
    // \n - це перехід на новий рядок
    printf("Обчислення значення функції для варіанту 10\n");
    printf("Задані значення: a = %.0f, b = %.0f, c = %.0f\n", a, b, c);

    // 3. Обчислення виразу
    // Важливо: множення позначаємо зірочкою (*), а ділення — слешем (/)
    // Пріоритет операцій такий самий, як у математиці (спочатку в дужках)
    float funct1 = (a * b + a * c - b * c) / (2 * a - b);
    float funct2 = 5 / (a + b);
    y = funct1 - funct2;

    // 4. Виведення кінцевого результату
    // %.4f означає, що ми виводимо число з 4 знаками після коми
    printf("Результат: y = %.4f\n", y);

    return 0; // Ознака успішного завершення програми
}